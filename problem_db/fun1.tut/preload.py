def increment(x):
    """
    Increment the argument (ie, return the next largest integer).

    Args:
      x (int): The number to increment.

    Returns:
      The next largest integer after the argument.

    """
    return x + 1


def double(x):
    """
    Double the argument.

    Args:
      x (int): The number to double.

    Returns:
      Double the argument.

    """
    return 2*x


